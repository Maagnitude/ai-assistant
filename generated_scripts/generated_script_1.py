def calculate_sum_and_division():
    num1 = float(input("Enter the first number: "))
    num2 = float(input("Enter the second number: "))
    num3 = float(input("Enter the third number: "))

    sum_of_numbers = num1 + num2 + num3
    division_result = sum_of_numbers / 20

    print("Sum of three numbers:", sum_of_numbers)
    print("Division result by 20:", division_result)

calculate_sum_and_division()